package coreJava;

public class CountingWhileLoop {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 50) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println();
        int j = 51;
        while (j <= 100) {
            System.out.print(j + " ");
            j++;

        }
    }
}
