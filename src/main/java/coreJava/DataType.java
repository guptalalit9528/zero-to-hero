package coreJava;

public class DataType {
    public static void main(String[] args) {
        System.out.println("( 8 )Primitive data type-------------------------------------------------------------");
        byte verySmall = 32;
        short small = 32000;
        int interger = 100000;
        long viewsCount = 10_00000_00_0L;
        float decimal = 10.5f;
        double bigFloat = 20.99;
        char letter = 'A';
        boolean inEligiable = true;

        System.out.println("Byte: " + verySmall);
        System.out.println("Short: " + small);
        System.out.println("Int: " + interger);
        System.out.println("Long: " + viewsCount);
        System.out.println("Float: " + decimal);
        System.out.println("Double: " + bigFloat);
        System.out.println("Char: " + letter);
        System.out.println("Boolean: " + inEligiable);


    }
}

